#include <stdio.h>
#include <stdlib.h>

int	main(int ac, char **av)
{
	int	av_01 = 0;
	int	av_02 = 0;

	if (ac == 3)
	{
		if ((av_01 = atoi(av[1])) > 0 && ((av_02 = atoi(av[2])) > 2))
		{
			while (av_01 != av_02)
			{
				if (av_01 > av_02)
					av_01 -= av_02;
				else
					av_02 -= av_02;
			}
			printf("%d", av_01);
		}
	}
	printf("\n");
	return (0);
}
