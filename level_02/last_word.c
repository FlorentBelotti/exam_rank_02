#include <unistd.h>

int	main(int ac, char **av)
{
	int	i = 0;
	int	temp = 0;

	if (ac == 2)
	{
		while (av[1][i])
		{
			if (av[1][i] == ' ' && av[1][i + 1] >= 33 && av[1][i + 1] <= 126)
				temp = i + 1;
			i++;
		}
		while (av[1][temp] != ' ' && av[1][temp])
		{
			write (1, &av[1][temp], 1);
			temp++;
		}
	}
	write (1, "\n", 1);
	return (0);
}
