#include <unistd.h>

int     main(int arc, char **arv)
{
        int     i = 0;

        if (arc == 2)
        {
		while (arv[1][i])
		{
			if (arv[1][i] >= 'A' && arv[1][i] <= 'Z')
				arv[1][i] = 90 - arv[1][i] + 65;
			else if (arv[1][i] >= 'a' && arv[1][i] <= 'z')
				arv[1][i] = 122 - arv[1][i] + 97;
			write (1, &arv[1][i], 1);
			i++;
		}
	}
	write (1, "\n", 1);
	return (0);
}
